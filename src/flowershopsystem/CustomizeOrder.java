    private void jMakeCustomizeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMakeCustomizeActionPerformed
        // TODO add your handling code here:
        CustomizeFlower customize = null;
        try {
            String flowerArrangementName = jStyle.getSelectedItem().toString();
            String ArrangementSize = jSize.getSelectedItem().toString();
            String flowertype = jFlowerType.getSelectedItem().toString();
            String accessories = jAccessories.getSelectedItem().toString();
            String priority = jPriority.getSelectedItem().toString();
            float price = Float.parseFloat(jTotal.getText());
            String custName = jCustName.getSelectedItem().toString();
            Customer c = null;

            for (int i = 0; i < MainMenu.custList.size(); i++) {
                if (MainMenu.custList.get(i).getName().equalsIgnoreCase(custName)) {
                    c = MainMenu.custList.get(i);
                }
            }

            customize = new CustomizeFlower(
                    flowerArrangementName, ArrangementSize, flowertype, accessories,
                    priority, price, c);

            if (jPriority.getSelectedItem().toString().equalsIgnoreCase("EXPRESS")) {
                MainMenu.customizeList.enqueueFront(customize);
            } else {
                MainMenu.customizeList.enqueue(customize);
            }

            JOptionPane.showMessageDialog(this, "Customize Order added");
            this.dispose();
        } catch (Exception ex) {
            System.out.println(ex.getMessage());
            JOptionPane.showMessageDialog(this, "Data input error", "Input Unsuccessful", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jMakeCustomizeActionPerformed

