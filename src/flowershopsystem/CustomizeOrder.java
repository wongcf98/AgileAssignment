    private void loadDataIntoDDL() {
        jFlowerType.removeAllItems();
        jCustName.removeAllItems();
        jFlowerType.addItem(" ");
        jCustName.addItem("Choose a customer");
        for (int i = 0; i < MainMenu.prodList.size(); i++) {
            jFlowerType.addItem(MainMenu.prodList.get(i).getName());
        }
        for (int i = 0; i < MainMenu.custList.size(); i++) {
            if (MainMenu.custList.get(i).getCustType().equalsIgnoreCase("CONSUMER")) {
                jCustName.addItem(MainMenu.custList.get(i).getName());
            }
        }
    }

    private void calculateTotal() {
        try {
            float sizeprice = Float.parseFloat(jSizePrice.getText());
            float flowerprice = Float.parseFloat(jFlowerPrice.getText());
            float addOnprice = Float.parseFloat(jAddOnPrice.getText());

            String total = String.valueOf(sizeprice + flowerprice + addOnprice);
            jTotal.setText(total);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Data input incomplete", "Input Unsuccessful", JOptionPane.ERROR_MESSAGE);
            jAccessories.setSelectedIndex(0);
        }
    }
