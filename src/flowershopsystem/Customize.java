    private void jDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jDeleteActionPerformed
        // TODO add your handling code here:
        try {
            dm = (DefaultTableModel) jTable1.getModel();
            int index = jTable1.getSelectedRow();

            String customizename = jStyle.getSelectedItem().toString();
            String customizeSize = jSize.getSelectedItem().toString();
            String customizeType = jFlowerType.getSelectedItem().toString();
            String customizeAccesories = jAccessories.getSelectedItem().toString();
            String customizePriority = jPriority.getSelectedItem().toString();
            float Total = Float.parseFloat(jTotal.getText());
            int customize_year = jCusDate.getCalendar().get(Calendar.YEAR);
            int customize_month = jCusDate.getCalendar().get(Calendar.MONTH);
            int customize_day = jCusDate.getCalendar().get(Calendar.DAY_OF_MONTH);

            Calendar customizeDate = Calendar.getInstance();
            customizeDate.set(Calendar.YEAR, customize_year);
            customizeDate.set(Calendar.MONTH, customize_month);
            customizeDate.set(Calendar.MONTH, customize_day);

            CustomizeFlower customize = new CustomizeFlower(customizename, customizeSize, customizeType, customizeAccesories, (float) Total, customizePriority, customizeDate);

            dm.removeRow(index);

            MainMenu.customizeList.dequeue();
            calculateSubTotal();
            JOptionPane.showMessageDialog(this, "Customize Order have been deleted successfully.");

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Delete Unsuccessful", "Delete Unsuccessful", JOptionPane.ERROR_MESSAGE);
        }
        jDelete.setEnabled(false);

    }//GEN-LAST:event_jDeleteActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        jAddCustomize.setEnabled(false);
        jDelete.setEnabled(true);
        int index = jTable1.getSelectedRow();
        TableModel model = jTable1.getModel();
        
        String id = (String) jTable1.getValueAt(index, 0);
        new CustomizeConfirm(id).setVisible(true);
        
        String customizename = model.getValueAt(index, 1).toString();
        String customizeSize = model.getValueAt(index, 2).toString();
        String customizeType = model.getValueAt(index, 3).toString();
        String customizeAccesories = model.getValueAt(index, 4).toString();
        String customizePriority = model.getValueAt(index, 5).toString();

        jStyle.setSelectedItem(customizename);
        jSize.setSelectedItem(customizeSize);
        jFlowerType.setSelectedItem(customizeType);
        jAccessories.setSelectedItem(customizeAccesories);
        jPriority.setSelectedItem(customizePriority);
        jAddCustomize.setEnabled(false);


    }//GEN-LAST:event_jTable1MouseClicked

