    private String saveCustomizeIntoArray() {
        CustomizeFlower customize = null;
        try {
            String flowerArrangementName = jStyle.getSelectedItem().toString();
            String ArrangementSize = jSize.getSelectedItem().toString();
            String flowertype = jFlowerType.getSelectedItem().toString();
            String accessories = jAccessories.getSelectedItem().toString();
            String priority = jPriority.getSelectedItem().toString();
            float price = Float.parseFloat(jTotal.getText());
            int customize_year = jCusDate.getCalendar().get(Calendar.YEAR);
            int customize_month = jCusDate.getCalendar().get(Calendar.MONTH);
            int customize_day = jCusDate.getCalendar().get(Calendar.DAY_OF_MONTH);

            Calendar customizeDate = Calendar.getInstance();
            customizeDate.set(Calendar.YEAR, customize_year);
            customizeDate.set(Calendar.MONTH, customize_month);
            customizeDate.set(Calendar.MONTH, customize_day);

            customize = new CustomizeFlower(flowerArrangementName, ArrangementSize, flowertype, accessories, price, priority, customizeDate);

            MainMenu.customizeList.enqueue(customize);
            if (!MainMenu.customizeList.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Customize Order added");
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Data input error", "Input Unsuccessful", JOptionPane.ERROR_MESSAGE);
        }
        return customize.id;

    }

    private void calculateSubTotal() {
        float currentTotal = Float.parseFloat(jTotal.getText());
        float subTotal = Float.parseFloat(jSubTotal.getText());
        subTotal += currentTotal;

        jSubTotal.setText(String.valueOf(subTotal));
    }

    private void calculateTotal() {
        float sizeprice = Float.parseFloat(jSizePrice.getText());
        float flowerprice = Float.parseFloat(jFlowerPrice.getText());
        float addOnprice = Float.parseFloat(jAddOnPrice.getText());

        String total = String.valueOf(sizeprice + flowerprice + addOnprice);
        jTotal.setText(total);
    }
